function J = jacob ( Q,L )
J = zeros(3,3);
J(1,1) = -sin(Q(1))*L(1)-sin(Q(1)+Q(2))*L(2)-sin(Q(1)+Q(2)+Q(3))*L(3);
J(1,2) = -sin(Q(1)+Q(2))*L(2)-sin(Q(1)+Q(2)+Q(3))*L(3);
J(1,3) = -sin(Q(1)+Q(2)+Q(3))*L(3);
J(2,1) = cos(Q(1))*L(1)+cos(Q(1)+Q(2))*L(2)+cos(Q(1)+Q(2)+Q(3))*L(3);
J(2,2) = cos(Q(1)+Q(2))*L(2)+cos(Q(1)+Q(2)+Q(3))*L(3);
J(2,3) = cos(Q(1)+Q(2)+Q(3))*L(3);
J(3,1) = 1;
J(3,2) = 1;
J(3,3) = 1;
end

